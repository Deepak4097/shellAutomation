#set -x
echo "find the first 10 biggest file in the file system passed via positional argument"
path="$1"
echo $path
du -ah $path | sort -hr | head -n5 > /tmp/filesize.txt
echo "this is the list of big files in the file system $path "
cat /tmp/filesize.txt

